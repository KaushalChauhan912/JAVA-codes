

public class Addition{

    public static void main(String[] args) {
        //simple addition
        int sum = 2+3;
        System.out.println("sum is " + sum);

        //increment in the value of variable
        int a=4;
        a++;
        System.out.println("after increment the number will be " + a);
        

        // More increment examples
        int s=5;
        s= s+3;
        System.out.println(s);
        //or
        s+=3;
        //both operations are same 
        System.out.println(s);
    }
}
