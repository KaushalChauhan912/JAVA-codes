

public class Subtract{

    public static void main(String[] args) {
        //simple subtraction
        int difference = 53-44;
        System.out.println("Difference  is " + difference);

        //decrement in the value of variable
        int a=4;
        a--;
        System.out.println("after decrement the number will be " + a);
        

        // More decrement examples
        int s=5;
        s= s-3;
        System.out.println(s);
        //or
        s-=3;
        //both operations are same 
        System.out.println(s);

        //similarly multiplication and division can be done using same methods
        //use '*' operator for multiplication and
        //use '/' operator for division..
    }
}
