public class escape {
    public static void main(String[] args) {
        //  "\" is used to escape special character in output...
        System.out.println("Hello \"World\"");
        // when "\\" is used then only one backslash is escaped and one remains in the output
        System.out.println(":c\\windows\\program files");
        // \n is used to move output to the next line
        //and \t is used to give space of one tab
        System.out.println(":c\nwindows\t\tprogram files");
    }
}
