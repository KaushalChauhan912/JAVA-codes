// A Java program to demonstrate comparison operators

public class comaprision_operator {

    public static void main(String[] args) {
        // Declare and initialize two integer variables
        int a = 34;
        int b = 45;

        // Check if a is equal to b using the '==' operator
        System.out.println("Equavalent : "+ (a == b));

        // Check if a is not equal to b using the '!=' operator
        System.out.println("Not Equavalent : "+ (a != b));

        // Check if a is less than b using the '<' operator
        System.out.println("a less than b : "+ (a < b));

        // Check if a is greater than b using the '>' operator
        System.out.println("a greater than b : "+ (a > b));

        // Check if a is less than or equal to b using the '<=' operator
        System.out.println("a less than equal to b : "+ (a <= b));

        // Check if a is greater than or equal to b using the '>=' operator
        System.out.println("a greater than equal to b : "+ (a >= b));
    }
}
